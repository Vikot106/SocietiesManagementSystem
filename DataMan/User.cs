            get => uname;
            set => uname = value;
        }

        public string Sid {
            get => sid;
            set => sid = value;
        }

        public string Gender {
            get => gender;
            set => gender = value;
        }

        public string Age {
            get => age;
            set => age = value;
        }

        public string Phone {
            get => phone;
            set => phone = value;
        }

        public string Depart {
            get => depart;
            set => depart = value;
        }

        public string Soc {
            get => soc;
            set => soc = value;
        }

        public string Upass {
            get => upass;
            set => upass = value;
        }

        public string Idenity {
            get => idenity;
            set => idenity = value;
        }
    }
}